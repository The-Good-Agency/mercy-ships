                        // Show/Hide amount_option
                        $('.amount_option ' + currentAttrValue).show().siblings().hide();
                 
                        // Change/remove current tab to active
                        $(this).parent('li').addClass('active').siblings().removeClass('active');
                 
                        e.preventDefault();
                    });
                });
